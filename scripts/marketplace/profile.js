export function buildProfileFormItems(translations) {
  const roleItems = [
    { value: "teacher", text: translations.get("Teacher") },
    { value: "student", text: translations.get("Student") }
  ];
  return [
    {
      dataField: "role",
      label: { text: translations.get("Role") },
      editorType: "dxSelectBox",
      editorOptions: {
        items: roleItems,
        valueExpr: "value",
        displayExpr: "text",
        showClearButton: true,
        placeholder: translations.get("Select a role")
      }
    },
    {
      dataField: "country",
      label: { text: translations.get("Country") },
      editorType: "dxSelectBox",
      editorOptions: {
        dataSource: countryItems,
        valueExpr: "value",
        displayExpr: "text",
        searchEnabled: true,
        showClearButton: true,
        placeholder: translations.get("Select a country")
      }
    },
    {
      dataField: "preferredLanguage",
      label: { text: translations.get("Language") },
      editorType: "dxSelectBox",
      editorOptions: {
        items: languageItems,
        valueExpr: "value",
        displayExpr: "text",
        placeholder: translations.get("Select a language")
      }
    }
  ];
}
